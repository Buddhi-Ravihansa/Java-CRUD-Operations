    public void LoadProductId(){
        try {
            pst = con.prepareStatement("SELECT product_id FROM product");
            rs = pst.executeQuery();
            cmbProductId.removeAllItems();
            while(rs.next()){
                cmbProductId.addItem(rs.getString(1));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(productInformation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
