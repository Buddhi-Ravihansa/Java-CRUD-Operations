    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            String productId = cmbProductId.getSelectedItem().toString();
            pst = con.prepareStatement("SELECT product_name,price,quantity FROM product WHERE product_id="+ productId);
            rs = pst.executeQuery();
            if(rs.next()){
                txtProductName.setText(rs.getString(1));
                txtPrice.setText(rs.getString(2));
                txtQuantity.setText(rs.getString(3));
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(productInformation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String productId = cmbProductId.getSelectedItem().toString();
            pst = con.prepareStatement("DELETE FROM product WHERE product_id="+ productId);
            int k = pst.executeUpdate();
            txtProductName.setText("");
                txtPrice.setText("");
                txtQuantity.setText("");
                txtProductName.requestFocus();
                LoadProductId();
            if(k==1){
                JOptionPane.showMessageDialog(this, "Record Deleted!");
            }else{
                JOptionPane.showMessageDialog(this, "Delete Failed!");
}
        } catch (SQLException ex) {
            Logger.getLogger(productInformation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String productName = txtProductName.getText();
            String quantity = txtQuantity.getText();
            String price = txtPrice.getText();
            
            pst = con.prepareStatement("INSERT INTO product(product_name, price, quantity) VALUES (?,?,?)");
            pst.setString(1, productName);
            pst.setString(2, price);
            pst.setString(3,quantity );
            int k  = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"Record Added!");
                txtProductName.setText("");
                txtPrice.setText("");
                txtQuantity.setText("");
                txtProductName.requestFocus();
                LoadProductId();
                
            }else{
                JOptionPane.showMessageDialog(this,"Record Failed!");

            }
                    } catch (SQLException ex) {
            Logger.getLogger(productInformation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

